package com.roberto.SCCyL.bean;

public class SanitariosAfectadosBean {

	private String fecha;
	private String provincia;
	private String ambito;
	private String categoria;
	private String testRealizados;
	private String testPositivos;
	private String aislamiento;
	private String alta;

	public String getFecha() {
		return fecha;
	}

	public void setFecha(String fecha) {
		this.fecha = fecha;
	}

	public String getProvincia() {
		return provincia;
	}

	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}

	public String getAmbito() {
		return ambito;
	}

	public void setAmbito(String ambito) {
		this.ambito = ambito;
	}

	public String getCategoria() {
		return categoria;
	}

	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}

	public String getTestRealizados() {
		return testRealizados;
	}

	public void setTestRealizados(String testRealizados) {
		this.testRealizados = testRealizados;
	}

	public String getTestPositivos() {
		return testPositivos;
	}

	public void setTestPositivos(String testPositivos) {
		this.testPositivos = testPositivos;
	}

	public String getAislamiento() {
		return aislamiento;
	}

	public void setAislamiento(String aislamiento) {
		this.aislamiento = aislamiento;
	}

	public String getAlta() {
		return alta;
	}

	public void setAlta(String alta) {
		this.alta = alta;
	}

}
