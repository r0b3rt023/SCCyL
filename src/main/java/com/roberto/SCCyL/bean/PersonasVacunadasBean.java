package com.roberto.SCCyL.bean;

public class PersonasVacunadasBean {

	private String fecha;
	private String provincia;
	private String personasVacunadas;

	public String getFecha() {
		return fecha;
	}

	public void setFecha(String fecha) {
		this.fecha = fecha;
	}

	public String getProvincia() {
		return provincia;
	}

	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}

	public String getPersonasVacunadas() {
		return personasVacunadas;
	}

	public void setPersonasVacunadas(String personasVacunadas) {
		this.personasVacunadas = personasVacunadas;
	}

}
